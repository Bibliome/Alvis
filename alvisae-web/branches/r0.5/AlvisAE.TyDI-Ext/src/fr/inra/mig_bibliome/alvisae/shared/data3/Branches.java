package fr.inra.mig_bibliome.alvisae.shared.data3;

import com.google.gwt.core.client.JavaScriptObject;
import com.google.gwt.core.client.JsArray;

/**
 * @author fpapazian
 */
public interface Branches {

    /**
     * @return the Semantic class from where the branches start
     */
    public SemClass getFromSemClass();
    
    /**
     * @return the Semantic class where to the branches end
     */
    public SemClass getToSemClass();
    

    /**
     * @return the path going from the "from" Semantic class to the "to" Semantic class
     */
    public  JsArray<? extends JavaScriptObject> getPaths();

}
