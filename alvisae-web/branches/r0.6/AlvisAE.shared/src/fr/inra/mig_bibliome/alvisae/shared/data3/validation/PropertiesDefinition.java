package fr.inra.mig_bibliome.alvisae.shared.data3.validation;

import java.util.Collection;

public interface PropertiesDefinition {
	/**
	 * Return either if the specified key is supported.
	 * @param key
	 * @return
	 */
	boolean isPropertyKeySupported(String key);
	
	/**
	 * Return all property definitions.
	 * Will not return null.
	 * Returned value is read-only.
	 * @return
	 */
	Collection<PropertyDefinition> getPropertyDefinitions();
}
