package fr.inra.mig_bibliome.alvisae.client.Document;

import com.google.gwt.core.client.JavaScriptObject;
import com.google.gwt.dom.client.Node;

/**
 *
 * @author fpapazian
 */
public class AnnotationMarkerIpml extends JavaScriptObject implements AnnotationMarker {

    protected AnnotationMarkerIpml() {
    }

    @Override
    public final native String getContainerId() /*-{ return this.containerId; }-*/;

    @Override
    public final native void setContainerId(String containerId) /*-{ this.containerId = containerId; }-*/;

    @Override
    public final native Node getNode() /*-{ return this.node; }-*/;

    @Override
    public final native void setNode(Node node) /*-{ this.node = node; }-*/;

    @Override
    public final native int getStartCharacterOffset() /*-{ return this.startOffset; }-*/;

    @Override
    public final native void setStartCharacterOffset(int startCharacterOffset) /*-{ this.startOffset = startCharacterOffset; }-*/;

    @Override
    public final native int getEndCharacterOffset() /*-{ return this.endOffset; }-*/;

    @Override
    public final native void setEndCharacterOffset(int endCharacterOffset) /*-{ this.endOffset = endCharacterOffset; }-*/;

    @Override
    public final native String getText() /*-{ return this.text; }-*/;

    @Override
    public final native void setText(String textvalue) /*-{ this.text = textvalue; }-*/;

    @Override
    public final native String getNodeId() /*-{ return this.node.id; }-*/;
}
